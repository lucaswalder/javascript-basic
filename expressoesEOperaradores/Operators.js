/* Expressões e Operadores

- Expressions

- Operators
    Binary
    Unitary
    Ternary

*/

// operador binário or Binary
let numberOne = 1
console.log(numberOne + 1)

// operador unitario or Unitary
let numberTwo = 2
console.log(++numberTwo) // ++ adiciona 1
// or
console.log(--numberTwo) // -- subtrai 1

// operador ternário or ternary
console.log(true ? 'Olá' : 'Até mais') //use true ou false para definir as opções
