
//Operadores Aritiméticos

// - multiplicação
console.log(12.5 * 25) // se usa o * para multiplicar 

// - divisão
console.log(12 / 2) // se usa / para dividir

// - adição
console.log(28 + 2) // se usa + para somar

// - subtração
console.log(33 - 2) // se usa / para subtrair

// -- resto da divisão
let reminder
reminder = 14 % 9  // se usa o sinal de % para pegar o restante da divisão
console.log(reminder)  //mostra o resto da divisão (nessa caso será 5)


// -- incremento
let increment = 0
increment++   // se usa o sinal de ++ para incrementar (sempre adicionando +1)
increment++ // pode ser usado mais de uma vez
console.log(increment)  //mostra o resultado (nessa caso será 2)

// -- decremento
let decrement = 0
decrement--   // se usa o sinal de -- para decrementar (sempre subtraindo 1)
decrement-- // pode ser usado mais de uma vez
console.log(decrement)  //mostra o resultado (nessa caso será -2)

// -- exponencial
console.log(3 ** 4)  //eleva o número ao seu expoente (nessa caso será 27)

